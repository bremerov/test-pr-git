def addNumbers(num1,num2):
    return num1 + num2

def substractNumbers(num1,num2):
    return num1 - num2

def productNumbers(num1,num2):
    return num1 * num2

som1=addNumbers(1,4)
som2=substractNumbers(1,4)
print(str(som1 + som2))

som=addNumbers(1,4)
product=productNumbers(1,4)
print(str(som + product))